//Usuarios
CREATE TABLE USUARIOS (
    ID_USUARIO NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(100) NOT NULL,
    CORREO VARCHAR2(100) NOT NULL UNIQUE,
    PASSWORD VARCHAR2(255) NOT NULL,
    IDIOMA VARCHAR2(20) DEFAULT 'Español',
    TEMA VARCHAR2(20) DEFAULT 'Azul',
    MODO_OSCURO NUMBER(1) DEFAULT 0,
    CONSTRAINT PK_USUARIOS PRIMARY KEY (ID_USUARIO)
);

//Categorias
CREATE TABLE CATEGORIAS (
    ID_CATEGORIA NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(100) NOT NULL,
    CONSTRAINT PK_CATEGORIAS PRIMARY KEY (ID_CATEGORIA)
);

//Videojuegos
CREATE TABLE VIDEOJUEGOS (
    ID_VIDEOJUEGO NUMBER GENERATED BY DEFAULT AS IDENTITY,
    TITULO VARCHAR2(150) NOT NULL,
    DESCRIPCION VARCHAR2(500),
    FECHA_LANZAMIENTO DATE,
    IMAGEN VARCHAR2(255),
    ID_CATEGORIA NUMBER,
    CONSTRAINT PK_VIDEOJUEGOS PRIMARY KEY (ID_VIDEOJUEGO),
    CONSTRAINT FK_VIDEOJUEGO_CATEGORIA
        FOREIGN KEY (ID_CATEGORIA)
        REFERENCES CATEGORIAS(ID_CATEGORIA)
);

//Favoritos
CREATE TABLE FAVORITOS (
    ID_FAVORITO NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_USUARIO NUMBER NOT NULL,
    ID_VIDEOJUEGO NUMBER NOT NULL,

    CONSTRAINT PK_FAVORITOS PRIMARY KEY (ID_FAVORITO),

    CONSTRAINT FK_FAVORITOS_USUARIO
        FOREIGN KEY (ID_USUARIO)
        REFERENCES USUARIOS(ID_USUARIO),

    CONSTRAINT FK_FAVORITOS_VIDEOJUEGO
        FOREIGN KEY (ID_VIDEOJUEGO)
        REFERENCES VIDEOJUEGOS(ID_VIDEOJUEGO)
);